    pythonw = python_exe.parent / "pythonw.exe"
    if pythonw.exists():
        python_exe = pythonw

    # Create a task that repeats every interval_min for (interval_min * repeat_count) duration
    duration_min = interval_min * repeat_count
    duration_str = f"PT{duration_min}M"
    interval_str = f"PT{interval_min}M"

