{os_block}
# Track repetitions via counter file
counter_path = r"{counter_path}"
count = json.loads(pathlib.Path(counter_path).read_text()) if pathlib.Path(counter_path).exists() else 0
count += 1
pathlib.Path(counter_path).write_text(json.dumps(count))
# If this was the last repetition, schedule self-deletion
